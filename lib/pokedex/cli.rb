    def menu 
        puts ""
        input = gets.strip.downcase 
        if input == "1"
            pokemon_list
        elsif input == "exit"
            puts ""
            exit_image 
            puts "Come back anytime!"
            puts ""
        else
            puts "Not sure what you meant there... Please try again!"
            menu 
        end
    end

    def pokemon_list 
        puts ""
        Pokemon.all.each_with_index do |pokemon, index| 
            puts "#{index + 1}. #{pokemon.name.capitalize}"
        end
        menu_two
    end

    def menu_two
        puts ""
        puts "Enter any pokemon name to view their attributes!"
        puts ""
        input = gets.strip.downcase 
        if Pokemon.find_by_name(input).empty? == false 
            selection(input)
        else
            puts ""
            puts "Not sure what you meant there... Please try again!"
            puts ""
            menu_two
        end
    end

    def selection(input)
        pokemon = API.get_data(input)
        puts ""
        puts "Name: #{pokemon.name.capitalize}"
        puts "-------------------"
        puts "Type: #{pokemon.type.capitalize}"
        puts "-------------------"
        puts "Abilities: #{pokemon.ability.capitalize}"
        puts "-------------------"
        puts "Moves: #{pokemon.moves.capitalize}"
        menu_three
    end

